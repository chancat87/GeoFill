const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.join(__dirname, '..');
const DATA_DIR = path.join(ROOT, 'data', 'address-pool');
const OUTPUT_FILE = path.join(ROOT, 'scripts', 'address-pool.js');

const REQUIRED_FIELDS = ['address', 'city', 'state', 'zipCode'];
const ALLOWED_FIELDS = new Set(REQUIRED_FIELDS);
const MIN_ENTRIES_PER_COUNTRY = 5;
const MIN_CITIES_PER_COUNTRY = 5;

function normalizeToken(value) {
  return String(value || '')
    .trim()
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/\s+/g, ' ');
}

function toCountryName(fileName) {
  return fileName
    .replace(/\.json$/i, '')
    .split('-')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function validateEntry(country, entry, index, seen) {
  for (const field of Object.keys(entry)) {
    if (!ALLOWED_FIELDS.has(field)) {
      throw new Error(`${country}[${index}] unknown field: ${field}`);
    }
  }

  for (const field of REQUIRED_FIELDS) {
    if (typeof entry[field] !== 'string') {
      throw new Error(`${country}[${index}] missing string field: ${field}`);
    }
  }

  if (!entry.address.trim()) {
    throw new Error(`${country}[${index}] address is empty`);
  }
  if (!entry.city.trim()) {
    throw new Error(`${country}[${index}] city is empty`);
  }

  const key = [entry.address, entry.city, entry.state, entry.zipCode]
    .map((value) => value.trim().toLowerCase())
    .join('|');
  if (seen.has(key)) {
    throw new Error(`${country}[${index}] duplicate address: ${entry.address}`);
  }
  seen.add(key);
}

function readCountryFile(fileName) {
  const country = toCountryName(fileName);
  const fullPath = path.join(DATA_DIR, fileName);
  const raw = fs.readFileSync(fullPath, 'utf8');
  const entries = JSON.parse(raw);

  if (!Array.isArray(entries)) {
    throw new Error(`${fileName} must contain an array`);
  }
  if (entries.length < MIN_ENTRIES_PER_COUNTRY) {
    throw new Error(`${country} should contain at least ${MIN_ENTRIES_PER_COUNTRY} addresses`);
  }

  const seen = new Set();
  entries.forEach((entry, index) => validateEntry(country, entry, index, seen));
  const cityCount = new Set(entries.map((entry) => normalizeToken(entry.city))).size;
  if (cityCount < MIN_CITIES_PER_COUNTRY) {
    throw new Error(`${country} should cover at least ${MIN_CITIES_PER_COUNTRY} cities, got ${cityCount}`);
  }

  return [country, entries];
}

function buildPool() {
  const files = fs.readdirSync(DATA_DIR)
    .filter((fileName) => fileName.endsWith('.json'))
    .sort();

  if (files.length === 0) {
    throw new Error(`No address pool JSON files found in ${DATA_DIR}`);
  }

  return Object.fromEntries(files.map(readCountryFile));
}

function writeRuntimeFile(pool) {
  const generatedAt = new Date().toISOString();
  const countries = Object.keys(pool);
  const totalEntries = countries.reduce((total, country) => total + pool[country].length, 0);
  const countryStats = Object.fromEntries(countries.map((country) => {
    const cityCount = new Set(pool[country].map((entry) => normalizeToken(entry.city))).size;
    return [country, {
      entries: pool[country].length,
      cities: cityCount
    }];
  }));
  const metadata = {
    generatedAt,
    countryCount: countries.length,
    totalEntries,
    minEntriesPerCountry: Math.min(...countries.map((country) => pool[country].length)),
    maxEntriesPerCountry: Math.max(...countries.map((country) => pool[country].length)),
    minCitiesPerCountry: Math.min(...Object.values(countryStats).map((stats) => stats.cities)),
    countryStats
  };
  const body = JSON.stringify(pool, null, 2);
  const metadataBody = JSON.stringify(metadata, null, 2);
  const output = `// Generated by scripts/build-address-pool.js. Do not edit by hand.\n` +
    `// Source: data/address-pool/*.json\n` +
    `// Generated at: ${generatedAt}\n` +
    `const LOCAL_VERIFIED_ADDRESS_POOL_DATA = ${body};\n\n` +
    `const LOCAL_VERIFIED_ADDRESS_POOL_META = ${metadataBody};\n\n` +
    `if (typeof window !== 'undefined') {\n` +
    `  window.LOCAL_VERIFIED_ADDRESS_POOL = LOCAL_VERIFIED_ADDRESS_POOL_DATA;\n` +
    `  window.LOCAL_VERIFIED_ADDRESS_POOL_META = LOCAL_VERIFIED_ADDRESS_POOL_META;\n` +
    `}\n\n` +
    `if (typeof globalThis !== 'undefined') {\n` +
    `  globalThis.LOCAL_VERIFIED_ADDRESS_POOL = LOCAL_VERIFIED_ADDRESS_POOL_DATA;\n` +
    `  globalThis.LOCAL_VERIFIED_ADDRESS_POOL_META = LOCAL_VERIFIED_ADDRESS_POOL_META;\n` +
    `}\n`;

  fs.writeFileSync(OUTPUT_FILE, output, 'utf8');
}

const pool = buildPool();
writeRuntimeFile(pool);
console.log(`Built ${OUTPUT_FILE} from ${Object.keys(pool).length} country files.`);
